    endereco_id NUMBER,
    email VARCHAR2(254) CONSTRAINT NN_LABORATORIOS_EMAIL NOT NULL,
    ativo CHAR(1) DEFAULT 'Y' CONSTRAINT NN_LABORATORIOS_ATIVO NOT NULL,
    acreditacao_iso17025 CHAR(1) DEFAULT 'N' CONSTRAINT NN_LABORATORIOS_ISO NOT NULL,
    registro_renasem VARCHAR2(30),
    credenciamento_mapa VARCHAR2(30),
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP CONSTRAINT NN_LABORATORIOS_CRIADO_EM NOT NULL,
    CONSTRAINT UQ_LABORATORIOS_CNPJ UNIQUE (cnpj),
    CONSTRAINT UQ_LABORATORIOS_EMAIL UNIQUE (email),
    CONSTRAINT CK_LABORATORIOS_CNPJ CHECK (REGEXP_LIKE(cnpj, '^[0-9]{14}$')),
    CONSTRAINT CK_LABORATORIOS_ATIVO CHECK (ativo IN ('Y','N')),
    CONSTRAINT CK_LABORATORIOS_ISO CHECK (acreditacao_iso17025 IN ('Y','N')),
    CONSTRAINT FK_LABORATORIOS_ENDERECOS FOREIGN KEY (endereco_id) REFERENCES enderecos(id)
)
~';

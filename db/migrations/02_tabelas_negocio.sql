    BEGIN
        EXECUTE IMMEDIATE q'~
CREATE TABLE fazendas (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_FAZENDAS PRIMARY KEY,
    nome VARCHAR2(150) CONSTRAINT NN_FAZENDAS_NOME NOT NULL,
    cpf_cnpj VARCHAR2(14) CONSTRAINT NN_FAZENDAS_CPF_CNPJ NOT NULL,
    endereco_id NUMBER,
    car VARCHAR2(30),
    area_total_ha NUMBER(12,2),
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP CONSTRAINT NN_FAZENDAS_CRIADO_EM NOT NULL,
    CONSTRAINT CK_FAZENDAS_CPF_CNPJ CHECK (REGEXP_LIKE(cpf_cnpj, '^[0-9]{11,14}$')),
    CONSTRAINT FK_FAZENDAS_ENDERECOS FOREIGN KEY (endereco_id) REFERENCES enderecos(id)
)
~';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE != -955 THEN
                RAISE;
            END IF;
    END;
    /
    BEGIN
        EXECUTE IMMEDIATE q'~
CREATE TABLE fazenda_usuarios (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_FAZENDA_USUARIOS PRIMARY KEY,
    fazenda_id NUMBER CONSTRAINT NN_FAZENDA_USUARIOS_FAZENDA_ID NOT NULL,
    usuario_id NUMBER CONSTRAINT NN_FAZENDA_USUARIOS_USUARIO_ID NOT NULL,
    papel VARCHAR2(20) CONSTRAINT NN_FAZENDA_USUARIOS_PAPEL NOT NULL,
    inicio_vigencia DATE CONSTRAINT NN_FAZENDA_USUARIOS_INI_VIG NOT NULL,
    fim_vigencia DATE,
    CONSTRAINT UQ_FAZENDA_USUARIOS UNIQUE (fazenda_id, usuario_id, papel, inicio_vigencia),
    CONSTRAINT CK_FAZENDA_USUARIOS_PAPEL CHECK (papel IN ('DONO','FUNCIONARIO','GERENTE')),
    CONSTRAINT CK_FAZENDA_USUARIOS_VIGENCIA CHECK (fim_vigencia IS NULL OR fim_vigencia >= inicio_vigencia),
    CONSTRAINT FK_FAZENDA_USUARIOS_FAZENDAS FOREIGN KEY (fazenda_id) REFERENCES fazendas(id),
    CONSTRAINT FK_FAZENDA_USUARIOS_USUARIOS FOREIGN KEY (usuario_id) REFERENCES usuarios(id)
)
~';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE != -955 THEN
                RAISE;
            END IF;
    END;
    /
    BEGIN
        EXECUTE IMMEDIATE q'~
CREATE TABLE talhoes (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY
        CONSTRAINT PK_TALHOES PRIMARY KEY,
    fazenda_id NUMBER CONSTRAINT NN_TALHOES_FAZENDA_ID NOT NULL,
    identificacao VARCHAR2(100) CONSTRAINT NN_TALHOES_IDENTIFICACAO NOT NULL,
    tipo_plantio VARCHAR2(30) CONSTRAINT NN_TALHOES_TIPO_PLANTIO NOT NULL,
    area NUMBER(12,2),
    profundidade_amostragem_cm NUMBER(5,2),
    textura_solo VARCHAR2(20),
    bioma VARCHAR2(20),
    latitude_centroide NUMBER(10,7),
    longitude_centroide NUMBER(10,7),
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP CONSTRAINT NN_TALHOES_CRIADO_EM NOT NULL,
    CONSTRAINT UQ_TALHOES_FAZENDA_IDENT UNIQUE (fazenda_id, identificacao),
    CONSTRAINT CK_TALHOES_TIPO_PLANTIO CHECK (tipo_plantio IN ('CONVENCIONAL','DIRETO','IRRIGADO','SEQUEIRO','OUTRO')),
    CONSTRAINT CK_TALHOES_TEXTURA_SOLO CHECK (textura_solo IS NULL OR textura_solo IN ('ARENOSA','MEDIA','ARGILOSA','MUITO_ARGILOSA')),
    CONSTRAINT CK_TALHOES_BIOMA CHECK (bioma IS NULL OR bioma IN ('AMAZONIA','CERRADO','CAATINGA','MATA_ATLANTICA','PAMPA','PANTANAL')),
    CONSTRAINT FK_TALHOES_FAZENDAS FOREIGN KEY (fazenda_id) REFERENCES fazendas(id)
)
~';
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE != -955 THEN
                RAISE;
            END IF;
    END;
